# Strongly Typed Languages
# Definition: In strongly typed languages, types are enforced strictly, meaning that variables and expressions have a specific type, and operations involving incompatible types will result in errors.
# Characteristics:
# Type Safety: Prevents operations on incompatible types, reducing bugs and improving program reliability.
# Explicit Conversions: Requires explicit type conversions (casting) to change a variable from one type to another.
# Compile-Time and Run-Time Checking: Type errors are often caught at compile time (if statically typed) or at run time (if dynamically typed).
# Examples:
# Statically Typed: Java, C#, Swift, Rust
# Dynamically Typed: Python, Ruby, JavaScript (though these are often considered "strongly dynamically typed")